
public class VariableDemo {

	public static void main(String[] args) {
		
		byte a = 127;
		System.out.println(a);
		
		final int b = 250;
		System.out.println(b);
		
		float c = 1.5f;
		System.out.println(c);
		
		char d = 'A';
		System.out.println(d);
		
		a=125;
		System.out.println(a);
		

	}

}
